import numpy as np
import matplotlib.pyplot as plt

def julia_set(width, height, x_min, x_max, y_min, y_max, c, max_iter):
    x, y = np.linspace(x_min, x_max, width), np.linspace(y_min, y_max, height)
    X, Y = np.meshgrid(x, y)
    Z = X + 1j * Y
    image = np.zeros(Z.shape, dtype=float)

    for i in range(max_iter):
        Z = Z**2 + c
        mask = np.abs(Z) < 1000
        image += mask

    return image

def plot_julia_set(image):
    plt.imshow(image, cmap='viridis', extent=(x_min, x_max, y_min, y_max))
    plt.colorbar()
    plt.title('Julia Set')
    plt.xlabel('Real')
    plt.ylabel('Imaginary')
    plt.show()

# Set parameters
width, height = 3200, 3200
x_min, x_max = -2, 2
y_min, y_max = -2, 2
c = -0.8 + 0.156j  # this complex constant is changable to generate different Julia sets
max_iter = 100

# Generate and plot Julia set
julia_image = julia_set(width, height, x_min, x_max, y_min, y_max, c, max_iter)
plot_julia_set(julia_image)

