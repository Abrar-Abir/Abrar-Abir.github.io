def create_image_array(image, cmap='viridis'):
    norm_image = (image - np.min(image)) / (np.max(image) - np.min(image))
    rgba_image = plt.get_cmap(cmap)(norm_image)
    return (rgba_image[:, :, :3] * 255).astype(np.uint8)

def save_image(image_array, file_path):
    im = Image.fromarray(image_array)
    im.save(file_path)

# Set parameters
